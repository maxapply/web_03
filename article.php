        <h1>单个牙齿缺失后怎么修复?</h1>
        <div class="time">时间：2020-09-08<span>来源：优贝口腔</span></div>
        <section>
          <p><i>精彩内容</i>&nbsp;&nbsp;&nbsp;&nbsp;单颗牙齿缺失后怎么修复呢？目前主要采用以下几个办法，固定桥、活动义齿、种植牙等几种方法，每种修复方式有自己的优缺点，所以建议大家还是要根据自己的牙齿情况来选择适合自己的方式会比较好，有专业医生指导，无需担心。</p>
          <h3>单个牙齿缺失后怎么修复</h3>
          <p><span>1、固定桥：</span>一般就是把两边的邻牙先磨小，磨小后的邻牙作为基牙，制作3个连冠的牙套戴在这两个基牙上，相当于中间的牙齿是被两边的牙齿支撑起来的。</p>
          <p>优缺点：颜色接近真牙，比较美观，而且稳定效果比活动假牙好。使用起来舒适稳定，但是需要磨小邻牙，而且好的材料价格高。</p>
          <p class="roll"><span>2、活动义齿：</span>使用金属钩固定在邻牙上的，也需要磨小邻牙。</p>
          <p>优缺点：价格较低，制作方便，可以自由摘戴。但是体积较大、较厚、不是很舒适，容易松动，咀嚼效果也不好，而且金属卡环不利于美观，容易造成食物嵌塞，邻牙蛀牙。</p>
          <p><span>3、种植牙：</span>是直接在牙齿缺失的地方打洞，放入人工牙根，然后在上部安装基台以及固定假牙等。</p>
          <p>优缺点：适应症广，可以修复单个牙，多个牙，全口无牙等，不破坏邻牙，有自己的牙根，稳定持久，咀嚼效果好，但是比较贵，需要有比较好的牙槽骨条件，可能要先做植骨。</p>
          <p>优贝口腔医院医生总结道：具体用什么方法修复，要根据患者不同的牙齿情况，和患者经济实力来选择。</p>
